kubectl get pods -n frontend -o wide

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "HORIZONTAL POD AUTOSCALERS"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "Backend HPA:"
kubectl get hpa -n backend

echo ""
echo "Frontend HPA:"
kubectl get hpa -n frontend

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "SERVICIOS (IPs PÚBLICAS)"
