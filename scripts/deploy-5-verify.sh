kubectl get svc --all-namespaces | grep -E 'LoadBalancer|NAMESPACE'

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "PULUMI OUTPUTS"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
cd "$(dirname "$0")/../infrastructure-k8s-deploy"
source venv/bin/activate
pulumi stack output

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "URL DEL FRONTEND"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

FRONTEND_URL=$(pulumi stack output frontend_url 2>/dev/null || echo "")

if [ -n "$FRONTEND_URL" ] && [ "$FRONTEND_URL" != "Pending..." ]; then
    echo "Aplicación disponible en:"
    echo "$FRONTEND_URL"
else
    echo "La IP pública aún se está asignando..."
fi

echo "=================================================="
echo "✅ VERIFICACIÓN COMPLETADA"
echo "=================================================="